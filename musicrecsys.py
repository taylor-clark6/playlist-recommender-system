"""<h1>Lyrical Similarity</h1>"""

# clean the lyrics
import re

def clean_text(text):
    text = text.lower()
    text = re.sub(r"[^a-z\s]", "", text)  # remove punctuation/numbers
    text = re.sub(r"\s+", " ", text).strip()
    return text

df_spotifyclean["clean_lyrics"] = df_spotifyclean["lyrics"].apply(clean_text)

# TF-IDF vectorizer of lyrics
from sklearn.feature_extraction.text import TfidfVectorizer

vectorizer = TfidfVectorizer(
    stop_words='english',
    ngram_range=(1, 2),   # bigrams included
    max_features=5000
)

tfidf_matrix = vectorizer.fit_transform(df_spotifyclean["clean_lyrics"])

# compute similarity matrix using cosine similarity
from sklearn.metrics.pairwise import cosine_similarity

similarity_matrix = cosine_similarity(tfidf_matrix)

"""<h1>Thematic Coherence</h1>"""

# use the elbow method to determine the optimal number of clusters
X = tfidf_matrix

import matplotlib.pyplot as plt
from sklearn.cluster import KMeans

inertia = []

for k in range(2, 15):
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    kmeans.fit(X)
    inertia.append(kmeans.inertia_)

plt.figure(figsize=(8,5))
plt.plot(range(2, 15), inertia, marker='o')
plt.xlabel("Number of Clusters (k)")
plt.ylabel("Inertia")
plt.title("Elbow Method for Optimal k")
plt.show()

"""K = 10 seems like approximately the elbow"""

# cluster with 10 clusters
NUM_CLUSTERS = 10

kmeans = KMeans(n_clusters=NUM_CLUSTERS, random_state=42)
df_spotifyclean["cluster"] = kmeans.fit_predict(tfidf_matrix)


df_spotifyclean= df_spotifyclean.loc[df_spotifyclean['lyrics'] != 'Lyrics for this song have yet to be released. Please check back once the song has been released.']
# index reset
df_spotifyclean = df_spotifyclean.reset_index(drop=True)

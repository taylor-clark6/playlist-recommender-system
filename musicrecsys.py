"""<h1>Emotional Arc</h1>"""

# create emotion labels

def get_emotion(row):
    if row["valence"] > 0.6 and row["energy"] > 0.6:
        return "happy_energetic"
    elif row["valence"] > 0.6:
        return "happy_calm"
    elif row["energy"] > 0.6:
        return "intense"
    else:
        return "sad_calm"

df_spotifyclean["emotion"] = df_spotifyclean.apply(get_emotion, axis=1)

# define emotional arcs (playlist "story")
EMOTIONAL_ARCS = {
    "chill_to_hype": [
        "sad_calm",
        "happy_calm",
        "happy_energetic"
    ],
    "hype_to_chill": [
        "happy_energetic",
        "happy_calm",
        "sad_calm"
    ]
}

# sort songs by emotional progression

def apply_emotional_arc(df_subset, arc_name="chill_to_hype"):
    arc = EMOTIONAL_ARCS[arc_name]

    emotion_rank = {e: i for i, e in enumerate(arc)}

    df_subset = df_subset.copy()

    df_subset["emotion_rank"] = df_subset["emotion"].map(
        lambda x: emotion_rank.get(x, len(arc))
    )

    return df_subset.sort_values(
        ["emotion_rank", "energy"]
    )

"""<h1>Generate Playlist</h1>"""

# song lookup function (song name to song index)

def get_song_index(song_name):
    matches = df_spotifyclean[
        df_spotifyclean["track_name"].str.lower() == song_name.lower()
    ]

    if len(matches) == 0:
        return None

    return matches.index[0]

# use similarity matrix to get similar songs
def get_similar_songs(song_idx, top_n=50):
    sim_scores = similarity_matrix[song_idx]

    # sort by similarity (highest first)
    similar_indices = sim_scores.argsort()[::-1]

    # remove the song itself
    similar_indices = similar_indices[1:top_n+1]

    return df_spotifyclean.iloc[similar_indices]

# combine with thematic coherence
def get_clustered_candidates(song_idx, top_n=50):
    cluster_id = df_spotifyclean.loc[song_idx, "cluster"]

    cluster_df = df_spotifyclean[
        df_spotifyclean["cluster"] == cluster_id
    ]

    return cluster_df

# combine similarity and cluster
def get_recommendations(song_idx, top_n=30):
    cluster_df = get_clustered_candidates(song_idx)

    # compute similarity within cluster only
    cluster_indices = cluster_df.index

    scores = similarity_matrix[song_idx][cluster_indices]

    cluster_df = cluster_df.copy()
    cluster_df["similarity"] = scores

    return cluster_df.sort_values("similarity", ascending=False)

# generate recommendations, bringing together similarity, cluster, and emotional arc
def generate_playlist(song_name, top_n=15, arc="chill_to_hype"):

    # 1. get index
    song_idx = get_song_index(song_name)

    if song_idx is None:
        return "Song not found"

    # 2. get recommendations (similar + cluster filtered)
    recs = get_recommendations(song_idx, top_n=50)

    # 3. apply emotional arc
    recs = apply_emotional_arc(recs, arc_name=arc)

    # 4. return final playlist
    return recs.head(top_n)[[
        "track_name",
        "track_artist",
        "emotion",
        "cluster"
    ]]

